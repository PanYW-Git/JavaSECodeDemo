package com.pyw.a05for_while_dowhile;

public class ForTest1 {
    public static void main(String[] args) {
        //TODO 打印1~5
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        //打印5~1
        for (int i = 5; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
